
public class DisplayRule 
{
	public DisplayRule()
	{
		
	}
	
	public void DisplayRules()
	{
		System.out.println(" [Object] ");
		System.out.println(" The object of the game is to become the wealthiest player through buying, renting and selling of property.");
		///////////////////////////////////////////////////////////////////////////
		System.out.println();
		///////////////////////////////////////////////////////////////////////////
		System.out.println(" [Equipment] ");
		System.out.println(" The equipment consists of a board, 2 dice, tokens, 32 houses and 12 Hotels. There are 16 Chance and 16 ");
		System.out.println(" Community Chest cards, 28 Title Deed cards (one for each property), and play money. ");
		///////////////////////////////////////////////////////////////////////////
		System.out.println();
		///////////////////////////////////////////////////////////////////////////
		System.out.println(" [The Bank]");
		System.out.println(" Besides the Bank's money, the Bank holds the Title Deeds, and the houses and hotels prior to purchase by the");
		System.out.println(" players. The Bank pays salaries and bonuses. It sells and auctions properties and hands out the proper Title Deed");
		System.out.println(" cards when purchased by a player, it also sells houses and hotels to the players and loans money when required on");
		System.out.println(" mortgages. The Bank collects all taxes, fines, loans and interest, and the price of all properties which it sells and");
		System.out.println(" auctions. The Bank never goes \"broke\". If the Bank runs out of money, the Banker may issue as much as needed");
		System.out.println(" by writing on any ordinary paper.");
		////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		////////////////////////////////////////////////////////////////////////////
		System.out.println(" [The Play]");
		System.out.println(" Starting with the Banker, each player in turn throws the dice. The player with the highest total starts the play. Place");
		System.out.println(" your token in the corner marked \"GO\", then throw the dice and move your token (in the direction of the arrow) the");
		System.out.println(" number of spaces indicated by the dice. After you have completed your play, the turn passes to the left. The tokens");
		System.out.println(" remain on the spaces occupied and proceed from that point on the player's next turn. Two or more tokens may rest");
		System.out.println(" on the same space at the same time. Depending on the space your token reaches, you may be entitled to buy real");
		System.out.println(" estate or other properties, or be obliged to pay rent, pay taxes, draw a Chance or Community Chest card, Go To");
		System.out.println(" Jail, or etc...");
		System.out.println(" ");
		System.out.println(" If you throw doubles, you move your token as usual, the sum of the two dice, and are subject to any privileges or");
		System.out.println(" penalties pertaining to the space on which you land. Retaining the dice, throw again and move your token as");
		System.out.println(" before. If you throw doubles three times in succession, move your token immediately to the space marked \"In Jail\".");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Go]");
		System.out.println(" Each time a player's token lands on or passes over Go, whether by throwing the dice or drawing a card, the Banker");
		System.out.println(" pays that player a $200 salary.");
		System.out.println(" ");
		System.out.println(" The $200 is paid only once each time around the board. However, if a player passing GO on the throw of the dice");
		System.out.println(" lands 2 spaces beyond it on Community Chest, or 7 spaces beyond it on Chance, and draws the \"Advance to GO\"");
		System.out.println(" instructions on the card.");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Buying Property]");
		System.out.println(" Whenever you land on an unowned property you may buy that property from the Bank at it's printed price. You");
		System.out.println(" receive the Title Deed card showing ownership. Place the title deed card face up in front of you. If you do not wish");
		System.out.println(" to buy the property, the Bank sells it at auction to the highest bidder. The high bidder pays the Bank the amount of");
		System.out.println(" the bid in cash and receives the Title Deed card for that property.");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Auctions]");
		System.out.println(" Any player, including the one who declined the option to buy it at the printed price, may bid. Bidding may start at any time.");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Paying Rent]");
		System.out.println(" When you land on a property that is owned by another player, the owner collects rent from you in accordance with");
		System.out.println(" the list printed on its Title Deed card.");
		System.out.println(" ");
		System.out.println(" If the property is mortgaged, no rent can be collected. When a property is mortgaged, its Title Deed card is placed");
		System.out.println(" face down in front of the owner.");
		System.out.println(" ");
		System.out.println(" It is an advantage to hold all the Title Deed cards in a color-group (i.e., Boardwalk and Park Place, or Connecticut,");
		System.out.println(" Vermont and Oriental Avenues) because the owner may then charge double rent for unimproved properties in that");
		System.out.println(" color-group. This rule applies to unmortgaged properties even if another property in that color-group is mortgaged. It");
		System.out.println(" is even more advantageous to have houses or hotels on properties because rents are much higher than for");
		System.out.println(" unimproved properties. The owner may not collect the rent if they fail to ask for it before the second player following");
		System.out.println(" throws the dice.");
		//////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		//////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Chance and Community Chest]");
		System.out.println(" When you land on either of these spaces, take the top card from the deck indicated, follow the instructions and");
		System.out.println(" return the card face down to the bottom of the deck. The \"Get Out of Jail Free\" card is held until used and then");
		System.out.println(" returned to the bottom of the deck. If the player who draws it does not wish to use it, then they may sell it, at any");
		System.out.println(" time, to another player at a price agreeable to both.");
		//////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Income Tax]");
		System.out.println(" If you land here you must pay $200 ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Super tax]");
		System.out.println(" If you land here you must pay $400");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////////////////
		System.out.println(" [Jail]");
		System.out.println(" You land in Jail when...");
		System.out.println(" ");
		System.out.println(" (1) Your token lands on the space marked \"Go to Jail\", (2) You draw a card marked \"Go to Jail\" or (3) You throw");
		System.out.println(" doubles three times in succession.");
		System.out.println(" ");
		System.out.println(" When you are sent to Jail you cannot collect your $200 salary in that move since, regardless of where your token is");
		System.out.println(" on the board, you must move directly into Jail. Your turn ends when you are sent to Jail.");
		System.out.println(" If you are not \"sent to jail\" but in the ordinary course of play lands on that space, you are \"Just Visiting\", you incur no");
		System.out.println(" penalty, and you move ahead in the usual manner on your next turn.");
		System.out.println(" ");
		System.out.println(" A player gets out of Jail by...");
		System.out.println("");
		System.out.println(" (1) Throwing doubles on any of your next three turns, if you succeed in doing this you immediately move forward the");
		System.out.println(" number of spaces shown by your doubles throw. Even though you had thrown doubles, you do not take another turn. ");
		System.out.println(" ");
		System.out.println(" (2) Using the \"Get Out of Jail Free Card\"");
		System.out.println(" ");
		System.out.println(" (3) Paying a fine of $50 before you roll the dice on either of your next two turns. If you do not throw doubles by your");
		System.out.println(" third turn, you must pay the $50 fine. You then get out of Jail and immediately move forward the number of spaces");
		System.out.println(" shown by your throw. Even though you are in Jail, you may buy and sell property, buy and sell houses and hotels");
		System.out.println(" and collect rents.");
		///////////////////////////////////////////////////////////////
		System.out.println(" ");
		///////////////////////////////////////////////////////////////
		System.out.println(" [Free Parking]");
		System.out.println(" A player landing on this place does not receive any money, property or reward of any kind.");
		System.out.println(" This is just a \"free\" resting place.");
		//////////////////////////////////////////////////////////////
		System.out.println(" ");
		//////////////////////////////////////////////////////////////
		System.out.println(" [Houses]");
		System.out.println(" When a player owns all the properties in a color-group they may buy Houses from the Bank and erect them on");
		System.out.println(" those properties. If you buy one house, you may put it on any one of those properties. The price");
		System.out.println(" you must pay the Bank for each house is shown on the side of the name of the property that you want to upgrade. ");
		System.out.println(" The owner still collects default rent from an opponent who lands on the unimproved properties of their complete color-group");
		System.out.println(" ");
		System.out.println(" Following the above rules, you may buy and erect at any time as many houses as your judgment and financial");
		System.out.println(" standing will allow.");
		//////////////////////////////////////////////////////////////
		System.out.println(" ");
		//////////////////////////////////////////////////////////////
		System.out.println(" [Hotels]");
		System.out.println(" When a player has four houses on each property of a complete color-group, they may buy a hotel from the Bank");
		System.out.println(" and erect it on any property of the color-group. They return the four houses from that property to the Bank and pays");
		System.out.println(" the price for the hotel as shown on the Title Deed card. Only one hotel may be erected on any one property.");
		System.out.println(" ");
		
		////////////////////////////////////////////////////////////////
		System.out.println(" ");
		////////////////////////////////////////////////////////////////
		System.out.println(" [Selling Property]");
		System.out.println(" Houses and Hotels may be sold back to the Bank at any time for one-half the price paid for them. All houses on one");
		System.out.println(" color-group may be sold at once, or they may be sold one house at a time (one hotel equals five houses).");
		System.out.println(" ");
		System.out.println(" Nobody can give their property to the Bank, so that means you are an anti-auctioner.");
		/////////////////////////////////////////////////////////////////
		System.out.println(" ");
		/////////////////////////////////////////////////////////////////
		System.out.println(" [Mortgages] ");
		System.out.println(" Unimproved properties can be mortgaged through the Bank at any time. Before an improved property can be");
		System.out.println(" mortgaged, all the buildings on all the properties of its color-group must be sold back to the Bank at half price. The");
		System.out.println(" mortgage value is printed on each Title Deed card.");
		System.out.println(" ");
		System.out.println(" No rent can be collected on mortgaged properties or utilities, but rent can be collected on unmortgaged properties in");
		System.out.println(" the same group.");
		System.out.println(" ");
		System.out.println(" In order to lift the mortgage, the owner must pay the Bank the amount of mortgage plus 10% interest. When all the");
		System.out.println(" properties of a color-group are no longer mortgaged, the owner may begin to buy back houses at full price.");
		System.out.println(" ");
		System.out.println(" The player who mortgages property retains possession of it and no other player may secure it by lifting the");
		System.out.println(" mortgage from the Bank. However, the owner may sell this mortgaged property to another player at an agreed");
		System.out.println(" price. If you are the new owner, you may lift the mortgage at once if you wish by paying off the mortgage plus 10%");
		System.out.println(" interest to the Bank. If the mortgage if not lifted at once, you must pay the Bank 10% interest when you buy the");
		System.out.println(" property and if you lift the mortgage later you must pay the Bank an additional 10% interest as well as the amount of");
		System.out.println(" the mortgage.");
		//////////////////////////////////////////////////////////
		System.out.println(" ");
		//////////////////////////////////////////////////////////
		System.out.println(" [Bankruptcy]");
		System.out.println(" You are declared bankrupt if you owe more than you can pay either to another player or to the Bank. If your debt is");
		System.out.println(" to another player, you must turn over to that player all that you have of value and retire from the game.");
		System.out.println(" In making this settlement, if you own houses or hotels, you must return these to the Bank in exchange for money to");
		System.out.println(" the extent of one-half the amount paid for them");
		System.out.println(" ");
		System.out.println(" This cash is given to the creditor. If you have mortgaged property you also turn this property over to your creditor");
		System.out.println(" but the new owner must at once pay the Bank the amount of interest on the loan, which is 10% of the value of the property.");
		System.out.println(" ");
		System.out.println(" The new owner who does this may then, at their option, pay the principal or hold the property until some later turn,");
		System.out.println(" then lift the mortgage. If they hold property in this way until a later turn, they must pay the interest again upon lifting");
		System.out.println(" the mortgage.");
		System.out.println(" ");
		System.out.println(" Should you owe the Bank, instead of another player, more than you can pay (because of taxes or penalties) even");
		System.out.println(" by selling off buildings and mortgaging property, you must turn over all assets to the Bank. In this case, the Bank");
		System.out.println(" immediately sells by auction all property so taken, except buildings. A bankrupt player must immediately retire from");
		System.out.println(" the game. The last player left in the game wins.");
		////////////////////////////////////////////////////////////
		System.out.println(" ");
		////////////////////////////////////////////////////////////
		System.out.println(" [Miscellaneous]");
		System.out.println(" Money can be loaned to a player only by the Bank and then only by mortgaging property. No player may borrow");
		System.out.println(" from or lend money to another player.");
		///////////////////////////////////////////////////////////////
	}
}
